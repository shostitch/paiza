package paiza.loop;

import java.util.Scanner;

public class loop3 {
	public static void main(String[] args) {
        // 自分の得意な言語で
        // Let's チャレンジ！！
        Scanner sc = new Scanner(System.in);
		for(int i = 0; i < 10; i ++) {
		    int num = sc.nextInt();
			System.out.println(num);
		}

		sc.close();
    }
}
